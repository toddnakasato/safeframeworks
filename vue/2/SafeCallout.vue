<!--
  SafeCallout — Vue 2 callout component.
  Renders via shared-mapping callout builder (./callout) — identical across
  frameworks. Structure + data-* only.
-->
<script lang="ts">
import type { ConfigBase, OnSafeEvent } from 'safecontracts';
import { defineComponent, type PropType } from 'vue';
import { createSafeCallout } from './callout';

export default defineComponent({
  name: 'SafeCallout',
  props: {
    config: { type: Object as PropType<ConfigBase>, required: true },
    onEvent: { type: Function as PropType<OnSafeEvent>, default: undefined },
  },
  data() {
    return { root: null as HTMLElement | null };
  },
  mounted() {
    const el = this.$refs.calloutContainer as HTMLElement;
    if (el) this.root = createSafeCallout(el, this.config, this.onEvent);
  },
  beforeDestroy() {
    this.root?.remove();
    this.root = null;
  },
});
</script>

<template>
  <div ref="calloutContainer"></div>
</template>
